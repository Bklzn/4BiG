class NutritionalValues{
    constructor(Energy, FatTotal, SaturatedFat, Carbohydrate, Sugars, Fibre, Protein, Salt){
        this.Energy = Energy
        this.FatTotal = FatTotal
        this.SaturatedFat = SaturatedFat
        this.Carbohydrate = Carbohydrate
        this.Sugars = Sugars
        this.Fibre = Fibre
        this.Protein = Protein
        this.Salt = Salt
    }
}